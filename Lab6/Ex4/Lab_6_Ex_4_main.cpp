#include "string.h"
#include <iostream>
using namespace std;

void main(void)
{

    // Обычные конструкторы
    cout << "1. Обычные конструкторы:\n";
    Stroka s1("Hello");
    Stroka s2;           // конструктор по умолчанию
    Stroka s3(s1);       // конструктор копирования
    cout << endl;
    
    // Конструктор с заданным размером памяти
    cout << "2. Конструктор с заданным размером:\n";
    Stroka s4(100);      // создаём строку с capacity=100
    cout << "   Ёмкость s4: " << s4.getCapacity() << endl;
    cout << "   Содержимое s4: \"";
    s4.vyvod();
    cout << "\"\n" << endl;
    
    // Присваивание строке с большим размером
    cout << "3. Присваивание строке с большим размером:\n";
    s4 = "Эта строка длиннее, но память уже выделена";
    cout << "   После присваивания: \"";
    s4.vyvod();
    cout << "\"\n   Длина: " << s4.dlena() << endl;
    cout << "   Ёмкость: " << s4.getCapacity() << endl;
    cout << "   (ёмкость не изменилась!)\n" << endl;
    
    // Демонстрация работы с маленьким буфером
    cout << "4. Создание строки с маленьким буфером:\n";
    Stroka s5(10);
    cout << "   Ёмкость s5: " << s5.getCapacity() << endl;
    s5 = "Hi";
    cout << "   После присваивания \"Hi\": ёмкость = " << s5.getCapacity() << endl;
    
    // Попытка записать строку, превышающую capacity
    cout << "   Введите строку (попробуйте ввести больше 10 символов): ";
    s5.vvod();
    cout << "   Вы ввели: \"";
    s5.vyvod();
    cout << "\"\n   Длина: " << s5.dlena() << endl;
    cout << "   Ёмкость: " << s5.getCapacity() << " (увеличилась автоматически)\n" << endl;
    
    // Сцепление строк
    cout << "5. Сцепление строк:\n";
    Stroka s6("Hello ");
    Stroka s7("World!");
    Stroka s8 = s6 + s7;
    cout << "   Результат: \"";
    s8.vyvod();
    cout << "\"\n" << endl;
}
